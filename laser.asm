
InitLaser
 ldd #0
 std sptr
 lbsr ROMson
 lbsr CPUInit
 lbsr InitFIRQ
 lbsr DisableFIRQ
 rts

FireLaser
 pshs u
 leau laser,pcr
 stu sptr
 lbsr EnableFIRQ
 puls u,pc

CPUInit

; INITIALIZATION REGISTER 0 $FF90
; 1    Coco 1/2 compatible: YES
; 0    MMU enabled: NO
; 0    GIME IRQ enabled: NO
; 1    GIME FIRQ enabled: YES
; 1    RAM at FExx is constant: YES
; 0    Standard SCS (spare chip select): OFF
; 00   ROM map control: 16k internal, 16K external

 ldb #%10011000			
 stb $FF90

; INITIALIZATION REGISTER 1 $FF91
; 0    Unused
; 0    Memory type 1=256K, 0=64K chips
; 1    TINS Timer clock source 1=279.365 ns, 0=63.695 us
; 0000 Unused
; 0    MMU task select: TASK0
 ldb #%00100000
 stb $ff91

; IRQ ENABLE/STATUS REGISTER $FF92
; 00	Undefined
; 0	Timer
; 0	HBORD Horizontal Border
; 0	VBORD Vertical Border
; 0	EI2 Serial Data
; 0	EI1 Keyboard
; 0	EI0 Cartridge
 ldb #%00000000
 stb $ff92

; FIRQ ENABLE/STATUS REGISTER $FF93
; 00	Undefined
; 1 	Timer
; 0 	HBORD Horizontal Border
; 0 	VBORD Vertical Border
; 0 	EI2 Serial Data
; 0 	EI1 Keyboard
; 0 	EI0 Cartridge
 ldb #%00100000
 stb $ff93

 rts

FIRQ
 pshs a,u
 ldu sptr	; pointer to sound data
 beq no@	; sample to play?
 lda ,u+	; get next sample
 bne yes@
 ldu #0		; last sample, clear pointer
 stu sptr
 lda 3,s	; disable FIRQ
 ora #$40
 sta 3,s
 bra no@
yes@
 ora #$02	; keep RS232 high
 sta $ff20	; save to DAC
 stu sptr	; save new pointer value
no@
 lda $FF93	; dismiss interrupt
 puls a,u
 rti

* Enable FIRQ
EnableFIRQ
 andcc #%10111111
 rts

* Disable IRQ and FIRQ
DisableFIRQ
 orcc #%01010000
 rts

InitFIRQ

 * Set FIRQ interrupt vector
 lda #$7e
 sta $fef4
 leau FIRQ,pcr
 stu $fef5

 * Disable HSYNC
 lda $ff01
 anda #$fe
 sta $ff01

 * Disable VSYNC
 lda $ff03
 anda #$fe
 sta $ff03

 * Enable TIMER
 * 1 / 6000 divided by 0.000000279 = 597
 ldd #597	; timer value (12 bit) 597 = 6000Hz
 std $ff94

 rts

* Map system ROMs into memory
ROMson
 sta $FFDE
 rts

